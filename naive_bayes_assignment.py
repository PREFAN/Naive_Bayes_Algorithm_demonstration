import pandas as pd
from collections import Counter
import numpy as np
#Create the dataset
data = pd.DataFrame({"MessageID": ["M1", "M2", "M3", "M4"],"MessageText": ["buy cheap now","limited offer buy","meet me now","let's catch up"],
                     "Class": ["Spam", "Spam", "Not Spam", "Not Spam"]})
print(data)

#Extract all unique words from the messages
def tokenize(text):
    return text.lower().split()
all_words = []
for msg in data["MessageText"]:
    all_words.extend(tokenize(msg))
vocab = sorted(set(all_words))
V = len(vocab)
print("Vocabulary:", vocab)
print("Vocabulary size V =", V)

#Calculate word frequencies for each class
spam_msgs = data[data["Class"] == "Spam"]["MessageText"]
ham_msgs = data[data["Class"] == "Not Spam"]["MessageText"]
spam_words = []
ham_words = []
for msg in spam_msgs:
    spam_words.extend(tokenize(msg))
for msg in ham_msgs:
    ham_words.extend(tokenize(msg))
spam_count = Counter(spam_words)
ham_count = Counter(ham_words)
print("Spam word frequencies:", spam_count)
print("Not Spam word frequencies:", ham_count)
total_spam_words = sum(spam_count.values())
total_ham_words = sum(ham_count.values())
print("Total spam words =", total_spam_words)
print("Total ham words  =", total_ham_words)

#Add the new message: "buy now"
new_message = "buy now"
new_tokens = tokenize(new_message)
print("New message tokens:", new_tokens)

#Calculate prior probabilities
num_spam = len(spam_msgs)
num_ham = len(ham_msgs)
total_msgs = len(data)
P_spam = num_spam / total_msgs
P_ham = num_ham / total_msgs
print("P(Spam) =", P_spam)
print("P(Not Spam) =", P_ham)

#Apply Multinomial Naïve Bayes with Laplace smoothing
def P_w_given_C(word, count_dict, total_words, V):
    return (count_dict.get(word, 0) + 1) / (total_words + V)
#Compute likelihoods
spam_likelihood = P_spam
ham_likelihood = P_ham
for w in new_tokens:
    spam_likelihood *= P_w_given_C(w, spam_count, total_spam_words, V)
    ham_likelihood *= P_w_given_C(w, ham_count, total_ham_words, V)
print("Likelihood Spam (unnormalized) =", spam_likelihood)
print("Likelihood Not Spam (unnormalized) =", ham_likelihood)

#Classification
if spam_likelihood > ham_likelihood:
    prediction = "Spam"
else:
    prediction = "Not Spam"
print("FINAL CLASSIFICATION:", prediction)

