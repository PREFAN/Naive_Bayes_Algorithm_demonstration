#Consider the dataset
data = {
    'text':[
        'Free money now',
        'Call now to claim your prize',
        'Meet me at the park',
        'Lets catch up later',
        'Win a new car today!',
        'Lunch plans?',
        'Congratulations! You won a lottery',
        'Can you send me the report?',
        'Exclusive offer for you',
        'Are you coming to the meeting?'],
    'label':['spam','spam','not sapm','not spam','spam','not spam','spam','not spam','spam', 'not spam']}
df = pd.DataFrame(data)
print(df)

#Map labels to Numerical Values
df['label'] = df['label'].map({'spam':1, 'not sapm':0})

from sklearn.model_selection import train_test_split
from sklearn.feature_extraction.text import CountVectorizer
from sklearn.naive_bayes import MultinomialNB
from sklearn.metrics import accuracy_score

#split the data into training and testing
X = df['text']
y = df['label']
X_train, X_test, y_train, y_test = train_test_split(X, y, test_size=0.3, random_state=42)

#Vectorize the text Data
vectorizer = CountVectorizer()
X_train_vec = vectorizer.fit_transform(X_train)
X_test_vec = vectorizer.transform(X_test)

#Train the NB model
model = MultinomialNB()
model.fit(X_train_vec, y_train)

#Predictions and Evaluate accuracy
y_pred = model.predict(X_test_vec)
accuracy = accuracy_score(y_test, y_pred)
print(f"Accuracy:{accuracy *100:.2f}%\n")

#Predict the message: "Congratulations, you've won a free vacation"
custom_message = ["Congratulations, you've won a free vacation"]
print(custom_message)
custom_message_vec = vectorizer.transform(custom_message)
prediction = model.predict(custom_message_vec)
print("Prediction for custom message:", "spam" if prediction[0]==1 else 'Not Spam')